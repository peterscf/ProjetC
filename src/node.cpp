	void node::append(node* N){
		if (get_child()==NULL){
			set_child(N);
		}
		else{
			node *TMP=get_child();
			while (TMP->get_bros()!=NULL){
				TMP=TMP->get_bros();
			}
			TMP->set_bros(N);
		}
	}
