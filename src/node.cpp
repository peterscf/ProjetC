	void  node::print_node(void) const{
		cout <<"-----------------------------\n";
		cout	<<"Node"<<endl
			<<"Value ="<<get_value()<<endl
			<<"Bros ="<<get_bros()<<endl
			<<"Child ="<<get_child()<<endl;
	}
