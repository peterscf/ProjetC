#ifndef NODE_H_
#define NODE_H_

#include <string>
#include <sstream>
#include<iostream>

using namespace std;

class node{
private:
// declaration des attributs
	
	string my_val;
	node* my_bros;
	node* my_child;


////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
public:
//Conctructeur
	node(string Val){
		my_val= Val;
		my_bros= NULL;
		my_child= NULL;
	}
//Destructeur
	~node(){};
////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
//Accesseurs et modifieurs
	node*  get_bros() const{
		return my_bros;	
	}
	const string & get_value() const{
		return my_val;	
	}
	node*  get_child() const{
		return my_child;	
	}
	
	void set_child(node* c){
		my_child=c;
	}
	void set_bros(node* b) {
		my_bros=b;
	}
////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
	void print_node(void) const;
	void append(node* N);
};
#endif
